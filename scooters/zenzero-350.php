		<p>
			ZenZero 350 не се нуждае от представяне, най-новият скутер на Benelli е перфектният другар за пътуване на средни и дълги разстояния,
			както и за каране в трудния градския трафик.
		</p>
		<p>
			Същността на ZenZero е незабавно разпознаваема: мощност, дизайн, внимание към консумацията на гориво и опазване на околната среда.
			Едноцилиндровия двигател е с водно охлаждане и инжекционна горивна уредба, която осигурява забележителна мощност и отличен комфорт на
			каране.
		</p>
		<p>
			Качеството на спирачната система и окачването е безспорно: два 256мм спирачни диска, 35мм вилка и два задни хидравлични амортисьора
			проектирани да осигурят максимален комфорт.
		</p>
		<p>
			Големите колела на ZenZero са гаранция за безопасност и при по-неравни настилки, 16" отпред и 15" отзад. Гумите осигуряват подходяща
			стабилност на пътя при всякакви условия. Таблото е изцяло дигитално и добре видимо на дневна светлина. Допълнителният 12V контакт е
			изключително полезен и перфектен за използване за навигационно устройство или за зареждане на мобилен телефон.
		</p>
		<p>
			ZenZero е подчертан и от фокуса върху неговият дизайн: широката предна обтекателна форма е основна стилистична черта на модела.
			Отделението за багаж под седалката е широко и може да съхранява две каски, разрешавайки категорично проблема със съхраняването
			им след паркиране. Възможно е да се сложи и оригинална задна чанта, в същия цвят като на скутера.
		</p>
