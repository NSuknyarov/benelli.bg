	<h4 class="text-green">Предвижване със стил</h4>
		<p>
			Caffenero е един от скутерите обичан за високата си надежност и маневреност, с големи 16" цолови колелета, неговият вид не
			може да бъде сбъркан.
		</p>
		<p>
			4-тактовия двигател, с 1-цилиндър и водно охлаждане, предлага висока производителност при всякакви условия, осигуряващи
			страхотен комфорт както за водача, така и за пътника.
		</p>
		<p>
			Рамата на Caffenero е изработена от закалени стоманени тръби, предното окачване използва хидравлична вилка, а отзад за комфорта
			работят два амортисьора. Дискови спирачки на двете колела предоставят спирачна сила достатъчна и в най-необходимите моменти.
		</p>
		<p>
			Широка и удобна, седалката на Caffenero е подходяща за 2-ма души, а пътника може да разчита на сгъваеми степенки за краката.
			Багажното отделение под седалката предоставя голям обем, а двете стойки - странична и централна са проектирани да разрешат
			всеки проблем с паркирането.
		</p>
		<p>
			Caffenero е наличен в цветовете: бяло, черно и бежово.
		</p>
