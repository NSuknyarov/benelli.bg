		<p>
			Не същия добре познат скутер: Macis е роден, за да бъде различен, с оригиналния си дизайн и ниската консумация на гориво.
			Той постига всичко това.
		</p>
		<p>
			Наличен в 125 и 150 кубикови варианти, този скутер изобилства с персоналност. Предната страна е спортна и енергична, а задната
			компактна и стройна. Фокусът върху дизайна се разкрива от капака на двигателя и особено от едно оригинално хрумване, което прави
			Macis абсолютно уникален, а именно "голото" алуминиево кормило, истински допир на елегантност.
		</p>
		<p>
			Macis е снабден с 4-тактов двигател с водно охлаждане, 16" цолови джанти, които предоставят както безопасност и скорост, така и
			гъвкавост при всякакви условия. Спирачната система е с две дискови спирачки, снабдени с широки дискове с диаметър от 240мм.
		</p>
		<p>
			Macis е не само качество и дизайн, неговата широка и удобна седалка предоставя комфорт при каране. Багажното отделение под седалката
			предлага изключително просторно място за съхранение на вашите принадлежности.
		</p>
