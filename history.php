		<p>
			Шест братя, една история. Шест мъже в легенда, която става реалност. Било пролетта на 1911г, когато Тереза Бенели,
			вдовица, ивестирала всичките семейни средства, за да основе работилница, която да даде сигурна работа на нейните шест
			сина, Джюзепе, Джовани, Филипо, Франческо, Доменико и Антонио "Тонино" Бенели.
		</p>
		<p>
			В началото работилницата била само сервиз, където съшо определени части за коли и мотоциклети били изработвани. Но шестте братя Бенели имали
			много по-висока амбиция: изработката на мотоциклети. Осем години по-късно, през 1919, първия двигател се бил родил, двутактов, с
			работен обем от 75 куб.см, прикрепен към рамка на велосипед, който в крайна сметка не произвел задоволителни резултати.
		</p>
		<p>
			През декември 1921 първият истински мотоциклет Benelli се появил на бял свят: “Velomotore”, мотор с лек двутактов двигател
			с работен обем от 98 куб.см, представен в две разновидности, туристически и спортен (125 куб.см), последван през 1923г от версия
			с работен обем от 147 куб.см, с който Тонино Бенели започнал да печели победи, които ще направят компанията от Пезаро всеизвестна
			в цяла Европа. 
		</p>
		<p>
			През 1926г. Джюзепе Бенели проектирал нов мотоциклет с 4-тактов двигател с работен обем от 175 куб.см, и мощност равна или дори
			по-голяма от моторите с по-голяма кубатура, което води и до неизброимо количество победи за Тонино Бенели, италиански шампион за
			годините 1927,1928,1930 и 1931.
		</p>
		<p>
			Увеличаването на производственият капацитет както и успешните продажби (175 кубиковия двигател бил използван в различни модели до 1934г,
			когато бил заместен от 500 и 250 кубиков 4-тактов вариант) довели до разширяването на завода, а през 1932г братята Бенели
			закупили постройките на дъскорезница Малорони в област ‘viale Principe Amedeo’, днешна ‘viale Mameli’.
		</p>
		<p>
			Две години по-късно, през 1934г, Бенели представили два нови съзтезателни мотоциклета: двукамерен 250 кубиков и вариант с 500 кубика.
			През 1940г Бенели представили модел с 500 кубика със странични разположени клапани, един фантастичен съзтезателен мотор, който
			никога не излиза на пистата, поради избухването на Втората световна война, която принуждава компанията да произвежда само
			мотори за военни цели.
		</p>
		<p>
			Марката от Пезаро била на върха на успеха (с 800 работници в завода), но Втората световна война унищожила всичко.
			Бомбардировките на съюзниците и унищожаването на документи от страна на Нацистите, редуцирали голямата компания до купчина
			отломки и празни помещения.
		</p>
		<p>
			Братята Бенели не изгубили вяра и веднага щом успели да възстановят машините и оборудването си, започнали преобразуването на
			около 1000 военни мотоциклета - основно от английски произход - изоставени на бойното поле от съюзниците, в такива за
			цивилна употреба. Също така през 1947г. били възстановени и състезателните мероприятия.
		</p>
		<p>
			1948г била ключова за Бенели: компанията наела мотоциклетния състезател Дарио Амброзини, а също така на 14 октомври братята Бенели
			обявили твърдото си решение да продължат с производството на мотоциклети. Спортните успехи на новоизградената компания Бенели взели връх през 1950г,
			когато Амброзини спечелил световния шампионат в клас до 250 кубикови мотоциклети. 
		</p>
		<p>
			Към края на 40-те, Джюзепе Бенели, поради непримирими разногласия със своите братя, напуска компанията. Това поставя
			началото на марката Motobi, с класически 2 и 4-тактови яйцевидни двигатели, с малка и средна кубатура. Последвали нови продажби и още
			спортни успехи за Benelli: повече от 1000 съзтезателни победи през 50-те и 60-те години.
		</p>
		<p>
			Производството на мотори продължило за Бенели и през 1951г представят модела ‘Leoncino’, чиито комерсиален
			успех бива последван и от спечелването на първото ‘Motogiro d’Italia’ през 1953г, със състезателя Тартарини зад кормилото.
		</p>
		<p>	
			През 1961г компанията от Пезаро отпразнува първата си 50 годишнина и на последвалата година, за да преборят кризата на
			мотоциклетната индустрия, двете марки Benelli и Motobi били обединени в една. Това били славно време, с нови разработки като
			4-цилиндровия 250 кубиков двигател и втора световна титла от 1969г благодарение на австралийския състезател Kelvin Carruthers. 
		</p>
		<p>
			Широка гама от модели характеризират производството на Benelli-Motobi от 60-те години: от обикновени скутери до мотоциклета "Торнадо", разполагащ
			с двуцилиндров двигател и работен обем от 650 куб.см. Това биват и последните истински творения на Benelli. През 1972г компанията бива
			закупена от аржентинския предприемач Алехандро де Томазо. Новият собственик рестартирал и разширил продуктовата гама с
			много-цилиндрови модели и престижен мотоциклет с 6-цилиндров 750 кубиков двигател (първият 6-тактов мотоциклет произведен
			серийно и предлаган на пазара), също така бива изграден нов и модерен завод.
		</p>
		<p>
			С течение на времето конкуренцията от Япония започнала да се увеличава и да предоставя повече напредначиви технологии. Западането било бавно и неизбежно. През
			1988г Бенели биват свалени на колене. Индустриалният магнат Джанкарло Селци, собственик на групата Biesse спасил славната
			компания от несигурното и бъдеще, и закупува Benelli на 23 октомври 1989г. Новопридобитата компания започнала да се цели в пазара на скутери
			с модели като Devil и Scooty. Изглеждало, че едно ново време е започнало, но след първите моменти на еуфория, перспективите отново
			станали несигурни.
		</p>
		<p>
			През 1995г, групата Merloni със собственик Фабриано закупува основния дял от историческата марка. Андреа Мерлони, син на Виторио,
			става ръководител на новата компания и свеж нов старт се дава с модели като скутера 491. Нови амбициозни проекти биват
			стартирани за скутери и дори спортни мотори като Торнадо 900, с 3-цилиндров двигател, който се състезава в Superbike
			шампионата. Но Бенели трябва да се изправи срещу нова криза.
		</p>
		<p>
			През декември 2005г. Бенели става част от Q.J group. Носител на награда за най-добър износител на мотоциклети за годината.
		</p>
