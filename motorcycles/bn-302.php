		<h5>Двигател</h5>
		<p>
		  2-цилиндров редови, 4-тактов, водно охлаждане, 4 клапана на цилиндър
		</p>
		<h5>Мощност</h5>
		<p>28 kW (38 cv) @ 12000 об/мин</p>
		<h5>Максимален въртящ момент</h5>
		<p>27 Nm (2,75 kgm) @ 9000 об/мин</p>
		<p>
			Новият BN302 е направен със страст за всички, от начинаещи до опитни водачи, които искат да
			изпитат удоволствието от управлението на двуколесна машина. Стилен и агресивен, BN302
			възнаграждава водача с окачване, изключително прецизен съединител, ръмжящ звук от
			двуцилиндровия редови двигател и ергономична стойка. Лесният контрол и сигурното спиране,
			правят този мотор перфектен за ежедневно предвижване по натоварените градски улици, но и
			за пътуване по дългите извънградски пътища.
		</p>
		<p>
			Зад невероятният външен вид стои двигател с мощност от 28kW (38 к.с.) при 12 000 об/мин и
			въртящ момент от 27 Nm (2.75 kgm) при 9000 об/мин. Двигателят е обоздаван от плавна
			6-скоростна предавателна кутия, която предоставя прецизно превключване на предавките
			всеки един момент.
		</p>
		<p>
			Вдъхновяващата ловкост на BN302 е поради подобрената технология в дизайна на здравата стоманена
			рама и избраната структура на окачването. Предната телескопична вилка е с диаметър от 41мм, а
			отзад регулируем амортисьор обира всяка неравност по пътя.
		</p>
		<p>
			За най-добра спирачна сила, BN302 използва двойка спирачни дискове отпред, с диаметър от 260мм,
			а на задното колело е мониран единичен диск с диаметър от 240мм.
		</p>
		<p>
			BN302 може да бъде открит в цветовете бяло, зелено и черно.
		</p>
