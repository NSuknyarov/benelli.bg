			@ 5000 rpm. The compression rate is of 11,6:1 and the electronic injection system has 53 mm throttle bodies.
		</p>
		<p>
			The decomposable frame of Tre-K Amazonas is characterised by the trestle steel front  and  adjoining rear  aluminium alloy
