		<h5>MAX. TORQUE</h5>
		<p>120 Nm (12.2 kgm) @ 8400 rpm</p>
		<p>
			Racing proud. TNT R, the most sporty Benelli bike does not come to a compromise and has now totally new looks matt grey. Absolute
			power and performance are its winning features to tackle the racetrack as well as the urban roads. TNT R is an hymn to performance.
		</p>
		<p>
			The engine of TNT R is the legendary in line triple which characterises the whole TNT range. The engine delivers here its maximum
			power and full riding potential with no restrictions to comfort: 157,8 hp (116 kW) @ 10200 rpm. and 120 Nm (12,2 kgm) of
			torque @ 8400 rpm. The features of the Benelli triple allow effective response at medium revs as a very few engines can do.
		</p>
		<p>
			Gearbox 6 speed with final chain drive, dry clutch with hydraulic command and anti-skipping device to prevent skidding
			are additional features of TNT R. The exhaust manifolds follow precisely the engine lines till the compact tail guard.
			The catalyst and the exhaust perfectly abate the noxious emissions and optimise the power supply at any speed.
		</p>
		<p>
			TNT R chassis makes a difference too: frame is made up of a trestle steel front and adjoining rear aluminium alloy cast.
			The shock absorbers are the best available on the market for racing use: the Marzocchi upside-down fork 50mm fully 
			adjustable and the ZF Sachs rear shock, also fully adjustable, to increase the performance of the rear suspension and
			allow TNT R to express all its riding power.
		</p>
		<p>
			The front tyre size is 120/70 and, at the rear, the rider can choose between 190/50 - series  tyre  -  and  two  further
			sizes,  180/55  and  200/50,  the  latter  especially indicated for racing. Tyres are mounted onto light and sporty aluminium
			alloy rims.
		</p>
		<p>
			The braking system is up the performances of TNT R: the two front wave discs 320 mm are matched with the Brembo 
			radial calipers, emblem of absolute efficiency, lightness and power, operated by a Brembo radial pump. At the rear 
			there is a wave disc 240 mm with twin piston Brembo brake caliper. The wheel base is a real racing feature: 1.443 mm, 
			which means agility in  the mixed terrain, quickness through the bends, bags of emotions under any condition. 
			The dry weight is of only 205 Kgs. Besides the new colour, TNT R is released also in white combined with red frame, 
			elegant and undisputably sporty at the same time.
		</p>
		<p>
			TNT R has truly unique accessories, such as: carbon muffler, handlebar ends in ergal, frame sliders, racing tank filler,
			carbon single-seat tail, motorbike covers and semi-rigid panniers.
		</p>
    
