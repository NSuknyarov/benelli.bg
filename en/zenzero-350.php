		</p>
		<p>
			The  big  wheels  of  ZenZero  are  a  guarantee  of  safety  even  on  the  uneven pavements, 16” on the front and of 15”
			at the rear, with tyres 120/70 and 140/70 able to offer a suitable road stability under any riding condition. The dashboard 
			is totally digital and well readable also by day. The 12 v plug is extremely useful and perfect to connect the satellite navigator 
			or recharge the mobile phone.
		</p>
		<p>
			The ZenZero is characterised also by the focus on design: the wide front fairing is an essential style feature and a repair 
			for the rider. The under seat compartment is spacious and can house two helmets resolving definitively the issue of their 
			transport after parking the scooter. It is also possible to have an original rear bag in the same colour of the scooter.
		</p>
