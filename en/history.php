		<p>
			Six brothers, one story. Six men for a legend become reality. It was the spring of 1911 when Teresa Benelli, widowed, 
			invested all the capital of her family to establish a workshop and ensure a stable job for her six sons, Giuseppe, 
			Giovanni, Filippo, Francesco, Domenico e Antonio “Tonino” Benelli.
		</p>
		<p>
			At the beginning it was only a service garage, where some spare parts for cars and motorcycles were also made. But the six Benelli
			brothers had a much higher ambition : building motorcycles. Eight years later, in 1919, the first engine was born, a two stroke 75cc
			applied to a bicycle frame which did not produce satisfying results.
		</p>
		<p>
			In December 1921 the first real Benelli motorcycle appeared : the “Velomotore”, 98cc two stroke lightweight bike presented in two
			models, Touring and Sport (125cc), followed in 1923 by a 147 cc version,  with which Tonino Benelli started to win the victories
			which will make the company of Pesaro renown throughout Europe.  
		</p>
		<p>
			In 1926 Giuseppe Benelli designed a new motorcycle, a 175cc 4-stroke engine bike, overhead camshaft commanded by an original
			‘four gear train’ and performances equal to or superior to the motorcycles of larger displacement, which would lead to countless 
			triumphs of  Tonino Benelli,  Italian champion in 1927, 1928, 1930 e 1931.
		</p>
		<p>
			The production increase and sales success (the 175cc was produced in different models until 1934, when a 500cc and a 250cc
			4-stroke motorcycle were presented) lead to the enlargement of the factory, and in 1932 the Benelli brothers bought the pavilions
			of the sawmill Molaroni in ‘viale Principe Amedeo’, which is now ‘viale Mameli’.
		</p>
		<p>
			Two years later, in 1934 Benelli introduced two new racing bikes: a 250 twin cam and a 500. In 1940 Benelli launched a 500cc
			with side valves and a fantastic racing bike (a 4-cylinder with double overhead camshaft and supercharger that would never be 
			used on the track) but the outbreak of the war forced the company to produce only military motorcycles.
		</p>
		<p>
			The company of Pesaro was at the height of success (800 employees worked in the factory) but the Second World War destroyed the factory.
			The allied bombing and the spoliations by the Nazis  reduced this large company to a pile of rubble and empty sheds.
		</p>
		<p>
			The Benelli brothers did not lose heart and, once retrieved machinery and equipments, started to convert about 1000 military
			motorcycles - mainly of English origin – left in the battlefields by the allies into bikes for civilian use. 1947 was also the
			year when the racing activities were resumed.   
		</p>
		<p>
			1948 was a milestone for Benelli : the company hired the motorcycle racer Dario Ambrosini and on October 14th the Benelli
			brothers announced the decision to continue producing motorcycles. The sports success of the new Benelli culminated in 1950
			in  Ambrosini’ victory of the World Championship in the 250 class.  
		</p>
		<p>
			At the end of  40’s Giuseppe Benelli, due to irreconcilable disagreements with his brothers, left the company.
			It was the beginning of Motobi, with the classic 2 and 4-stroke egg-shaped engine, of  small and medium displacement.
			A new sales and sports success : more than 1000 racing victories in the 50’s and 60’s. 
		</p>
		<p>
			The manufacturing activity of Benelli continued in 1951 with the presentation of the ‘Leoncino’ whose great commercial
			success was at its peak when Benelli won the first ‘Motogiro d’Italia’ in 1953 with the racer Tartarini.
		</p>
		<p>
			In 1961 the company of Pesaro celebrated its first 50 years and the following year, to tackle the crisis of the motorcycle
			industry, the two brands Benelli and Motobi were merged into one. This was the heroic time of  Provini e Pasolini, of the
			4-cylinder 250cc and of the second world title in 1969 with the Australian racer Kelvin Carruthers.
		</p>
		<p>
			A wide range of models characterised the production Benelli-Motobi in the 60’s: from scooters to the ‘Tornado’, a maxi
			bike 2-cylinder 650cc), the last original creation of Benelli. In 1972 the company was bought by the Argentinean entrepreneur
			Alejandro De Tomaso. The new property relaunched and enlarged the product range presenting multi-cylinder motorcycles and
			a prestigious 750cc 6-cylinder (the first 6-stroke motorcycle produced in series available for purchase by the general public)
			and built a new and modern factory.
		</p>
		<p>
			The Japanese competition became increasingly fierce and technically advanced. The decline was slow and yet inevitable.
			In 1988 Benelli was brought on its knees. The industrial tycoon Mr. Giancarlo Selci, owner of the Biesse group, saved
			the glorious company from an uncertain future and bought Benelli on October 23th, 1989. The company targeted the scooter
			market segment with the models Devil and Scooty. It seemed that a new time had begun, but after the first moments of euphoria
			the prospects became uncertain again.
		</p>
		<p>
			In 1995, the Merloni Group of Fabriano purchased the majority stake of the historical brand. Andrea Merloni,
			the son of Vittorio, was the leader of the new company and the fresh start arrived with the aggressive scooter 491.
			New ambitious projects, scooters and even sportsbikes: the Tornado 900cc three-cylinder which will also compete in
			the Superbike championship and the TnT 1130cc. But Benelli had soon to face up a new crisis again.
		</p>
		<p>
			In December 2005 Benelli became part of the group Q.J. Winner of the award as best motorcycle exporter of the year.
		</p>
