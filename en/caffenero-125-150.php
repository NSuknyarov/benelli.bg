	<h4 class="text-green">Move With Style</h4>
		<p>
			A long-time certainty of Benelli, Caffènero is one of the scooters more loved for the high reliability and maneuverability, 
			thanks to its 16”wheels, and for its looks absolutely unmistakable.
		</p>
		<p>
			The 4 stroke engine, single-cylinder liquid cooled, offer high performances under any condition, ensuring great comfort 
			for both rider and passenger.
		</p>
		<p>
			The frame of Caffènero is made of resilient steel tubes, the front suspension has a 33 mm hydraulic fork and a double hydraulic
			shock absorber at the rear. Disc brakes for both wheels for stopping in total safety even in limited spaces.
		</p>
		<p>
			Spacious and comfortable, the seat of Caffènero is fit for 2 people and the passenger can rely on wide folding
			footrest. The under seat area has a good capacity, and the double stand – side and central – are designed to prevent 
			dany parking problems.
		</p>
		<p>
			Caffènero in 2013 will be  available in  the following colours: white, black and cream. Among the accessories there
			are windshield and rear bag kit. Caffènero 125 and 150 is on sale at Dealers' shops.
		</p>
