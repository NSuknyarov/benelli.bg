	<h4 class="text-green">Ride and Believe It</h4>
		<p>
			Not the same old scooter: Macis was born to be different from its competitors of the same market segment for the originality
			of its design as well as for the limited consumption. And it did it.
		</p>
		<p>
			Released in the 125 and 150 displacements, this scooter abounds with personality. The front fairing is sporty and energetic,
			the rear is compact and slender, enriched with the bag holder. The focus on design is revealed by the engine cover, but especially
			by  one  original  feature  which  makes  Macis  absolutely  unique:  the 'naked' aluminium handlebar, a true touch of elegance.
		</p>
		<p>
			Macis has a 4 stroke engine liquid cooled, 16‚Äù wheels which ensure both safety and speed as well agility under any condition.
			The brake system has discs of wide diameter, 240 mm, with twin piston floating caliper.
		</p>
		<p>
			Macis is not only build quality and design. Its seat is wide and comfortable for an excellent riding position and the under
			seat space is extremely spacious.
		</p>
		<p>
			The colour of Macis 125 and 150 are white and black.
