		<p>
			QuattronoveX has personality in abundance and is the 50 cc more sporting manufactured by Benelli, 
			one of the scooter more loved by the   young for its appealing design which reminds to the bikes of large displacement.
		</p>
		<p>
			Its aluminium rims, the design of the lights and of the indicators prove its aggressive nature and strong personality.
			QuattronoveX has a single-cylinder 2 stroke engine air cooled. The frame is made of highly resilient steel while the 
			front suspension  is  an  hydraulic  fork  and  the  rear  suspension  is  equipped  with adjustable hydraulic shock absorber.
		</p>
		<p>
			The sporting character of QuattronoveX cannot prescind from the safety on the road, one of the key values of the Pesaro make.
			This scooter Benelli has two brake discs, one on the front and one at the rear, the first of 190 mm and the second of 180 mm,
			to ensure a good stopping capacity under any condition. The seat is wide and spacious, suitable for any kind of rider and allows 
			a really comfortable riding position. 
		</p>
