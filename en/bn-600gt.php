		<p>
			After the debut of new four-cylinder engine, for Benelli Q.J. the time has come to launch the GT version of the last naked born in Pesaro:
			the BN 600 GT. It is a forward-looking sport touring, mid-sized, the intelligent and smart motorcycle suitable for any type of rider, 
			even for those approaching for the first time in the world's great journeys, with the advantage to ride a 600cc, which means immediate guarantee 
			of cost and consumption.
		</p>
		<p>
			A bike easy, comfortable, versatile, with soft and sinuous forms, with which anyone can find a super confidence, appreciating immediately all 
			the qualities. A perfect vehicle for those who, riding the two wheels, runs throughout the year many miles: BN 600 GT in fact can be considered 
			as a viable alternative to the car for the life of every day and on weekends, this Benelli motorcycle became the ideal travel companion to 
			escape from the routine of the city.
		</p>
		<p>
			The heart of the BN 600 GT is the inline four-cylinder 4 stroke engine, liquid cooled, already seen in BN600R. It has a power 
			of 82 hp (60 kW) @ 11.000 rpm and max. torque of 55 Nm @ 8000 rpm, with electronic injection and four ø 38 mm throttle bodies. 
			Wet sump lubrication, wet clutch, 6 speeds gearbox and final chain drive. It has also a double overhead camshaft and 4 valves 
			for each cylinder. BN 600 GT has a 6-speed racing gearbox with precise, well-spaced gear engagement to achieve maximum performance.
		</p>
		<p>
			To differentiate it from the other naked of series is undoubtedly the decomposable frame: the front is a steel trestle, the rear aluminium alloy casting, 
			to lighten the frame. All of these features of BN 600 GT ensures to the rider a smooth and pleasant guide.
		</p>
		<p>
			Suspension system is made of an upside-down front fork ø 50mm and a rear aluminium alloy swingarm with rear shock absorber 
			in extension hydraulic and spring preload adjustment.
		</p>
		<p>
			Maximum attention to safety with a top quality braking system with front ø 320 mm twin floating disk and radial callipers
			and rear ø 260 mm single disk with double piston calliper.
		</p>
		<p>
			BN 600 GT has aluminium alloy 17’’ rims with 120/70 and 180/55 tyres. The dry weight is 223kg. The 27L capacity of the
			fuel tank is innovative and very important to allow the BN 600 GT rider to travel very long journeys in serenity.
		</p>
		<p>
			BN 600 GT is presented in the colours white and black and will be available starting by mid-2014 at a really competitive price. 
		</p>
