		<p>
			Calling it entry-level would be quite an understatement. Benelli BN302 is in fact much, much more. It's definitely a bike 
			designed for the young riders. A bike created with the intent to satisfy who fell in love for the first time on two wheels 
			more sporty and can no longer do without it. But once rode the BN302, even the most experienced rider, who drive usually 
			vehicles of all sizes, power and displacement, have fun with a bike like that.
		</p>
		<p>
			Slim, light and handy, BN302 is the two wheels which take confidence immediately. In the curves it is fast and snappy. 
			In the rise and fall it is agile and responsive, behaving like a big motorcycle. In the city, it becomes a excellent 
			ally to “fight” the traffic of metropolis, where BN302 is undoubtedly able to distinguish itself from direct competitors.
		</p>
		<p>
			The engine of BN302 is an in line two-cylinder, 4-stroke, with electronic fuel injection and timing system with overhead
			double camshaft and 4 valves. Signify performance are seen in power and torque data, respectively 37 Hp (27 kW) 
			at 11500 rpm and 27 Nm (2.75 kgm) at 9000 rpm. Wet sump lubrication, oil bath clutch, 6-speed transmission with 
			chain final drive add to the ingredients.
		</p>
		<p>
			The fairing allows BN302 to have very sleek lines, for a feeling of great dynamism, even at the sight. Careful and elegant
			finishes and the light unit, classic and modern at the same time, complete a framework of absolute quality.
		</p>
		<p>
			The frame is a steel trestle which, together with the size of the vehicle, provides the best manoeuvrability. 
			The suspension system is entrusted to an inverted hydraulic damping shock absorber front fork ø 41mm and to 
			a rear swingarm in steel pipes with rear shock absorber and spring preload adjustment.
		</p>
		<p>
			The braking system of Benelli BN302 is of the high quality, to guarantee absolute safety in any condition: 
			in front there is a twin disc of ø 260 mm, in the rear a single disc of ø 240 mm. The 17” aluminium alloy rims mount 120/70 and 160/60 tyres.
		</p>
		<p>
			The tank has a good 16 liters capacity.
		</p>
		<p>
			BN 302 comes in orange and white and will be available next year second half. The price, sure to be very competitive, is still being defined. 
